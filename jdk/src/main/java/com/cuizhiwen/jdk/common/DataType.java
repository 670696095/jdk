public class DataType {
    /**
     * 数据类型
     * @param args
     */
    public static void main(String[] args) {
        /**
         * 8位 -128 ~  127
         */
        byte b = -128;
        /**
         * 16位 -32768 ~ 32767
         */
        short s = -32768;
        /**
         * 32位
         */
        int i = 1;
        /**
         * 64位
         */
        long l = 10000L;
        /**
         * 浮点型 32位
         */
        float f = 234.5f;
        double d = 123.5;
        double d1 = 3.14E3;
        final double PI = 3.1415927;
        /**
         * 字符型 16 位 Unicode 字符 0~65535
         */
        char c ='A';
        char c1 = 65535;
        /**
         * 布尔型
         */
        boolean boo = true;


    }
}
