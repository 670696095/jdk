public class Annotations {
    /**
     * Annotation：注解
     *              1>可在编译，类加载 ，运行时被读取并执行相应的处理(不改变原有逻辑情况下，在源文件中嵌入一些补充信息进行验证或部署)
     *
     *              2>通过反射获取指定元素的Annotation对象，通过该对象获取注解中的元数据。
     *
     * @Retentain 指定被修饰的对象可以保存多久
     * @Target    指定能修饰哪些程序单元
     * @Documented 可用javadoc提取成文档
     * @Inherited  具有继承性，父类使用子类自动使用。
     *
     * @SuppressWarnings 则表示忽略指定警告
     */
}
